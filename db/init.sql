CREATE INDEX IF NOT EXISTS idx_auth_audit_logs_player_id
  ON auth_audit_logs (player_id);
CREATE INDEX IF NOT EXISTS idx_auth_audit_logs_guest_id
  ON auth_audit_logs (guest_id);
CREATE INDEX IF NOT EXISTS idx_auth_audit_logs_event_type
  ON auth_audit_logs (event_type);
CREATE INDEX IF NOT EXISTS idx_auth_audit_logs_created_at
  ON auth_audit_logs (created_at);

CREATE TABLE IF NOT EXISTS security_audit_logs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  event_type varchar(64) NOT NULL,
  target_type varchar(32) NULL,
  target_value varchar(256) NULL,
  client_ip varchar(64) NULL,
  severity varchar(16) NOT NULL DEFAULT 'warning',
  details_json jsonb NULL,
  created_at timestamptz NOT NULL DEFAULT current_timestamp
);

COMMENT ON COLUMN security_audit_logs.target_type IS 'ip, account, ticket, etc.';
COMMENT ON COLUMN security_audit_logs.target_value IS 'ip address, login_name, etc.';
COMMENT ON COLUMN security_audit_logs.severity IS 'info, warning, critical';

CREATE INDEX IF NOT EXISTS idx_security_audit_logs_event_type
  ON security_audit_logs (event_type);
CREATE INDEX IF NOT EXISTS idx_security_audit_logs_target
  ON security_audit_logs (target_type, target_value);
CREATE INDEX IF NOT EXISTS idx_security_audit_logs_client_ip
  ON security_audit_logs (client_ip);
CREATE INDEX IF NOT EXISTS idx_security_audit_logs_severity
  ON security_audit_logs (severity);
CREATE INDEX IF NOT EXISTS idx_security_audit_logs_created_at
  ON security_audit_logs (created_at);

