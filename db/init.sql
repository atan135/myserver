CREATE TABLE IF NOT EXISTS metrics_archive (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  service_name varchar(64) NOT NULL,
  bucket_time integer NOT NULL CHECK (bucket_time >= 0),
  qps integer NOT NULL DEFAULT 0 CHECK (qps >= 0),
  latency_ms integer NOT NULL DEFAULT 0 CHECK (latency_ms >= 0),
  online_value integer NOT NULL DEFAULT 0 CHECK (online_value >= 0),
  extra jsonb NULL,
  created_at timestamptz NOT NULL DEFAULT current_timestamp
);

COMMENT ON COLUMN metrics_archive.bucket_time IS '5-second bucket unix timestamp in seconds';
COMMENT ON COLUMN metrics_archive.online_value IS 'online_sessions / online_players / connections, etc.';
COMMENT ON COLUMN metrics_archive.extra IS 'service specific fields';

CREATE INDEX IF NOT EXISTS idx_metrics_archive_service_time
  ON metrics_archive (service_name, bucket_time);

CREATE TABLE IF NOT EXISTS player_inventory (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id varchar(64) NOT NULL,
  level integer NOT NULL DEFAULT 1,
  hp bigint NOT NULL DEFAULT 0,
  inventory_data jsonb NOT NULL,
  warehouse_data jsonb NOT NULL,
  equipment_data jsonb NOT NULL,
  attr_base_data jsonb NOT NULL,
  visual_data jsonb NOT NULL,
  buffs_data jsonb NOT NULL,
  updated_at timestamptz NOT NULL DEFAULT current_timestamp,
  created_at timestamptz NOT NULL DEFAULT current_timestamp,
  CONSTRAINT uk_player_inventory_player_id UNIQUE (player_id)
);

CREATE INDEX IF NOT EXISTS idx_player_inventory_updated_at
  ON player_inventory (updated_at);

DROP TRIGGER IF EXISTS trg_player_inventory_updated_at ON player_inventory;
CREATE TRIGGER trg_player_inventory_updated_at
  BEFORE UPDATE ON player_inventory
  FOR EACH ROW
  EXECUTE FUNCTION set_current_timestamp_updated_at();

