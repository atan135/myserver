\connect myserver_auth

CREATE TABLE IF NOT EXISTS player_accounts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id varchar(64) NOT NULL,
  guest_id varchar(128) NULL,
  login_name varchar(64) NULL,
  display_name varchar(64) NULL,
  account_type varchar(32) NOT NULL DEFAULT 'guest',
  status varchar(32) NOT NULL DEFAULT 'active',
  ban_expires_at timestamptz NULL,
  password_algo varchar(32) NULL,
  password_salt varchar(128) NULL,
  password_hash char(128) NULL,
  created_at timestamptz NOT NULL DEFAULT current_timestamp,
  last_login_at timestamptz NOT NULL DEFAULT current_timestamp,
  CONSTRAINT uk_player_accounts_player_id UNIQUE (player_id),
  CONSTRAINT uk_player_accounts_guest_id UNIQUE (guest_id),
  CONSTRAINT uk_player_accounts_login_name UNIQUE (login_name)
);

CREATE INDEX IF NOT EXISTS idx_player_accounts_last_login_at
  ON player_accounts (last_login_at);

CREATE TABLE IF NOT EXISTS auth_audit_logs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id varchar(64) NULL,
  guest_id varchar(128) NULL,
  event_type varchar(32) NOT NULL,
  access_token_hash char(64) NULL,
  ticket_hash char(64) NULL,
  client_ip varchar(64) NULL,
  details_json jsonb NULL,
  created_at timestamptz NOT NULL DEFAULT current_timestamp
