use sqlx::postgres::{PgDatabaseError, PgPool, PgPoolOptions};
use tracing::info;

use crate::config::Config;
use crate::core::inventory::Item;
use crate::core::inventory::player_data::PlayerData;
use crate::core::inventory::{
    AttrPanel, Buff, EquipmentSlots, ItemContainer, PlayerAttr, PlayerVisual,
};

const UNIQUE_VIOLATION: &str = "23505";

const CHARACTER_SCHEMA_STATEMENTS: &[&str] = &[
    r#"CREATE TABLE IF NOT EXISTS character_inventory (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        character_id varchar(64) NOT NULL,
        level integer NOT NULL DEFAULT 1,
        hp bigint NOT NULL DEFAULT 0,
        inventory_data jsonb NOT NULL,
        warehouse_data jsonb NOT NULL,
        equipment_data jsonb NOT NULL,
        attr_base_data jsonb NOT NULL,
        visual_data jsonb NOT NULL,
        buffs_data jsonb NOT NULL,
        updated_at timestamptz NOT NULL DEFAULT current_timestamp,
        created_at timestamptz NOT NULL DEFAULT current_timestamp,
        CONSTRAINT uk_character_inventory_character_id UNIQUE (character_id)
    )"#,
    "CREATE INDEX IF NOT EXISTS idx_character_inventory_updated_at ON character_inventory (updated_at)",
    r#"CREATE TABLE IF NOT EXISTS character_inventory_grants (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        request_id varchar(128) NOT NULL,
        character_id varchar(64) NOT NULL,
        source varchar(64) NOT NULL,
        items_json jsonb NOT NULL,
        reason varchar(255) NULL,
        created_at timestamptz NOT NULL DEFAULT current_timestamp,
        CONSTRAINT uk_character_inventory_grants_request_id UNIQUE (request_id)
    )"#,
    "CREATE INDEX IF NOT EXISTS idx_character_inventory_grants_character_id ON character_inventory_grants (character_id)",
];

/// PostgreSQL Character Inventory Store.
/// 负责角色背包数据的持久化。
#[derive(Clone)]
pub struct PgPlayerStore {
    pool: Option<PgPool>,
}

impl PgPlayerStore {
    /// 创建一个禁用的 store（用于测试和本地无数据库模式）。
    pub fn new_disabled() -> Self {
        Self { pool: None }
    }

    pub async fn new(config: &Config) -> Result<Self, Box<dyn std::error::Error>> {
        if !config.db_enabled {
            return Ok(Self { pool: None });
        }

        let pool = PgPoolOptions::new()
            .max_connections(config.db_pool_size.max(1))
            .connect(&config.database_url)
            .await?;

        if let Err(error) = initialize_schema(&pool).await {
            pool.close().await;
            return Err(Box::new(error));
        }

        info!("PgPlayerStore initialized");
        Ok(Self { pool: Some(pool) })
    }

    pub fn enabled(&self) -> bool {
        self.pool.is_some()
    }

    pub async fn close(&self) {
        if let Some(pool) = &self.pool {
            pool.close().await;
        }
    }

    pub async fn save(&self, character_id: &str, data: &PlayerData) -> Result<(), String> {
        let Some(pool) = &self.pool else {
            return Err("database not enabled".to_string());
        };

        let json = serialize_player_data(data)?;

        sqlx::query(UPSERT_CHARACTER_INVENTORY_SQL)
            .bind(character_id)
            .bind(data.level)
            .bind(data.get_hp())
            .bind(json.inventory)
            .bind(json.warehouse)
            .bind(json.equipment)
            .bind(json.attr_base)
            .bind(json.visual)
            .bind(json.buffs)
            .execute(pool)
            .await
            .map_err(|e| e.to_string())?;

        info!(character_id = %character_id, "character inventory saved");
        Ok(())
    }

    pub async fn save_with_grant_record(
        &self,
        character_id: &str,
        data: &PlayerData,
        request_id: &str,
        source: &str,
        reason: &str,
        items: &[Item],
    ) -> Result<bool, String> {
        let Some(pool) = &self.pool else {
            return Err("database not enabled".to_string());
        };

        if request_id.trim().is_empty() {
            self.save(character_id, data).await?;
            return Ok(true);
        }

        let json = serialize_player_data(data)?;
        let items_json = serde_json::to_value(items).map_err(|e| e.to_string())?;
        let mut tx = pool.begin().await.map_err(|e| e.to_string())?;

        match sqlx::query(
            r#"INSERT INTO character_inventory_grants (
                request_id,
                character_id,
                source,
                items_json,
                reason
            ) VALUES ($1, $2, $3, $4, $5)"#,
        )
        .bind(request_id)
        .bind(character_id)
        .bind(source)
        .bind(items_json)
        .bind(reason)
        .execute(&mut *tx)
        .await
        {
            Ok(_) => {}
            Err(error) if is_duplicate_request_error(&error) => {
                tx.rollback().await.map_err(|e| e.to_string())?;
                return Ok(false);
            }
            Err(error) => {
                tx.rollback().await.map_err(|e| e.to_string())?;
                return Err(error.to_string());
            }
        }

        sqlx::query(UPSERT_CHARACTER_INVENTORY_SQL)
            .bind(character_id)
            .bind(data.level)
            .bind(data.get_hp())
            .bind(json.inventory)
            .bind(json.warehouse)
            .bind(json.equipment)
            .bind(json.attr_base)
            .bind(json.visual)
            .bind(json.buffs)
            .execute(&mut *tx)
            .await
            .map_err(|e| e.to_string())?;

        tx.commit().await.map_err(|e| e.to_string())?;
        info!(character_id = %character_id, request_id = %request_id, "character inventory grant saved");
        Ok(true)
    }

    pub async fn load(&self, character_id: &str) -> Result<Option<PlayerData>, String> {
        let Some(pool) = &self.pool else {
            return Err("database not enabled".to_string());
        };

        let row = sqlx::query_as::<_, CharacterInventoryRow>(
            r#"SELECT
                level,
                hp,
                inventory_data,
                warehouse_data,
                equipment_data,
                attr_base_data,
                visual_data,
                buffs_data
            FROM character_inventory
            WHERE character_id = $1"#,
        )
        .bind(character_id)
        .fetch_optional(pool)
        .await
        .map_err(|e| e.to_string())?;

        row.map(|row| row.into_player_data(character_id))
            .transpose()
    }

    pub async fn delete(&self, character_id: &str) -> Result<(), String> {
        let Some(pool) = &self.pool else {
            return Err("database not enabled".to_string());
        };

        sqlx::query(r#"DELETE FROM character_inventory WHERE character_id = $1"#)
            .bind(character_id)
            .execute(pool)
            .await
            .map_err(|e| e.to_string())?;

        info!(character_id = %character_id, "character inventory deleted");
        Ok(())
    }
}

const UPSERT_CHARACTER_INVENTORY_SQL: &str = r#"INSERT INTO character_inventory (
    character_id,
    level,
    hp,
    inventory_data,
    warehouse_data,
    equipment_data,
    attr_base_data,
    visual_data,
    buffs_data
) VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $9)
ON CONFLICT (character_id) DO UPDATE SET
    level = EXCLUDED.level,
    hp = EXCLUDED.hp,
    inventory_data = EXCLUDED.inventory_data,
    warehouse_data = EXCLUDED.warehouse_data,
    equipment_data = EXCLUDED.equipment_data,
    attr_base_data = EXCLUDED.attr_base_data,
    visual_data = EXCLUDED.visual_data,
    buffs_data = EXCLUDED.buffs_data,
    updated_at = current_timestamp"#;

struct SerializedPlayerData {
    inventory: serde_json::Value,
    warehouse: serde_json::Value,
    equipment: serde_json::Value,
    attr_base: serde_json::Value,
    visual: serde_json::Value,
    buffs: serde_json::Value,
}

fn serialize_player_data(data: &PlayerData) -> Result<SerializedPlayerData, String> {
    Ok(SerializedPlayerData {
        inventory: serde_json::to_value(&data.inventory).map_err(|e| e.to_string())?,
        warehouse: serde_json::to_value(&data.warehouse).map_err(|e| e.to_string())?,
        equipment: serde_json::to_value(&data.equipment).map_err(|e| e.to_string())?,
        attr_base: serde_json::to_value(&data.attr.base).map_err(|e| e.to_string())?,
        visual: serde_json::to_value(&data.visual).map_err(|e| e.to_string())?,
        buffs: serde_json::to_value(&data.buffs).map_err(|e| e.to_string())?,
    })
}

#[derive(sqlx::FromRow)]
struct CharacterInventoryRow {
    level: i32,
    hp: i64,
    inventory_data: serde_json::Value,
    warehouse_data: serde_json::Value,
    equipment_data: serde_json::Value,
    attr_base_data: serde_json::Value,
    visual_data: serde_json::Value,
    buffs_data: serde_json::Value,
}

impl CharacterInventoryRow {
    fn into_player_data(self, character_id: &str) -> Result<PlayerData, String> {
        let inventory: ItemContainer =
            serde_json::from_value(self.inventory_data).map_err(|e| e.to_string())?;
        let warehouse: ItemContainer =
            serde_json::from_value(self.warehouse_data).map_err(|e| e.to_string())?;
        let equipment: EquipmentSlots =
            serde_json::from_value(self.equipment_data).map_err(|e| e.to_string())?;
        let attr_base: AttrPanel =
            serde_json::from_value(self.attr_base_data).map_err(|e| e.to_string())?;
        let visual: PlayerVisual =
            serde_json::from_value(self.visual_data).map_err(|e| e.to_string())?;
        let buffs: Vec<Buff> =
            serde_json::from_value(self.buffs_data).map_err(|e| e.to_string())?;

        let mut attr = PlayerAttr::new();
        attr.set_base(attr_base);

        let mut player_data = PlayerData::with_capacity(
            character_id.to_string(),
            inventory.capacity(),
            warehouse.capacity(),
        );
        player_data.inventory = inventory;
        player_data.warehouse = warehouse;
        player_data.equipment = equipment;
        player_data.level = self.level;
        player_data.attr = attr;
        player_data.visual = visual;
        player_data.buffs = buffs;
        player_data.set_hp(self.hp);

        player_data.clear_attr_dirty();
        player_data.clear_visual_dirty();
        player_data.clear_data_dirty();

        info!(character_id = %character_id, "character inventory loaded");
        Ok(player_data)
    }
}

fn is_duplicate_request_error(error: &sqlx::Error) -> bool {
    error
        .as_database_error()
        .and_then(|error| error.try_downcast_ref::<PgDatabaseError>())
        .is_some_and(|error| error.code() == UNIQUE_VIOLATION)
}

async fn initialize_schema(pool: &PgPool) -> Result<(), sqlx::Error> {
    sqlx::query("SELECT 1").execute(pool).await?;

    for statement in CHARACTER_SCHEMA_STATEMENTS {
        sqlx::query(statement).execute(pool).await?;
    }

    Ok(())
}
